public class Main {
  public static void main(String[] args) {
    int integerNumber = 20;       // 정수(int)
    double decimalNumber = 175.5; // 실수(double)
    char singleCharacter = 'A';   // 문자 1개(char)
    String textString = "Hello";    // 문자열(String)
    boolean isTrueOrFalse = true; // 참/거짓(boolean)

    System.out.println(integerNumber);
    System.out.println(decimalNumber);
    System.out.println(singleCharacter);
    System.out.println(textString);
    System.out.println(isTrueOrFalse);
  }
}

/*
출력
20
175.5
A
Hello
true
*/
